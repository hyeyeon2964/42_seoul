#include <stdio.h>

int		ft_str_is_uppercase(char *str)
{
	char a;

	if (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		a = *(str++);
		if (a < 'A' || a > 'Z')
			return (0);
	}
	return (1);
}
