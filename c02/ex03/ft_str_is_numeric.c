#include <unistd.h>

int		ft_str_is_numberic(char *str)
{
	char	a;

	if (*str == '\0')
		return (1);
	while (*(str) != '\0')
	{
		a = *(str++);
		if (a >= '0' && a <= '9')
		{
			continue;
		}
		else
			return (0);
	}
	return (1);
}
