int		ft_str_is_lowercase(char *str)
{
	char a;

	if (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		a = *(str++);
		if (a < 'a' || a > 'z')
			return (0);
	}
	return (1);
}
